var currentWordIndex;
var currentWord;

function init(){
    Array.prototype.push.apply(words, getWords());
    
    currentWordIndex = getRandomInt(0, words.length);
    currentWord = words[currentWordIndex]; 
}

function start(){
    init();

    speak(currentWord);    
}

